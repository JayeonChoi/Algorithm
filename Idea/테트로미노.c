/*출처 : 백준알고리즘
문제

폴리오미노란 크기가 1×1인 정사각형을 여러 개 이어서 붙인 도형이며, 다음과 같은 조건을 만족해야 한다.
•정사각형은 서로 겹치면 안된다.
•도형은 모두 연결되어 있어야 한다.
•정사각형의 꼭짓점끼리 연결되어 있어야 한다.즉, 변과 꼭짓점이 맞닿아있으면 안된다.

정사각형 4개를 이어 붙인 폴리오미노는 테트로미노라고 하며, 5가지가 있다.

아름이는 크기가 N×M인 종이 위에 테트로미노 하나를 놓으려고 한다.종이는 1×1 크기의 칸으로 나누어져 있으며, 각각의 칸에는 정수가 하나 쓰여 있다.

테트로미노 하나를 적절히 놓아서 테트로미노가 놓인 칸에 쓰여 있는 수들의 합을 최대로 하는 프로그램을 작성하시오.

테트로미노는 반드시 한 정사각형이 정확히 하나의 칸을 포함하도록 놓아야 하며, 회전이나 대칭을 시켜도 된다.


입력


첫째 줄에 종이의 세로 크기 N과 가로 크기 M이 주어진다. (4 ≤ N, M ≤ 500)

둘째 줄부터 N개의 줄에 종이에 쓰여 있는 수가 주어진다.i번째 줄의 j번째 수는 위에서부터 i번째 칸, 왼쪽에서부터 j번째 칸에 쓰여 있는 수이다.입력으로 주어지는 수는 1, 000을 넘지 않는 자연수이다.


출력

첫째 줄에 테트로미노가 놓인 칸에 쓰인 수들의 합의 최댓값을 출력한다.*/

//테트로미노
#include <stdio.h>
int N, M;
int map[510][510];
int dr[] = { -1, 1, 0, 0, -1 };
int dc[] = { 0, 0, -1, 1, 1 };
int nd[] = { 2, 1, 4, 4, 4, 4 };
int lookup[6][4][3] = {
	{ { 3, 3, 3 }, { 1, 1, 1 } }, \
	{ { 3, 1, 2 } }, \
	{ {1, 1, 3}, { 1, 1, 2 }, { 1, 3, 3 }, { 1, 2, 2 } }, \
	{ {1, 3, 1}, { 1, 2, 1 }, { 3, 0, 3 }, { 3, 1, 3 } }, \
	{ {3, 1, 4}, { 4, 1, 3 }, { 4, 1, 1 }, { 1, 1, 4 }}, \
	{ {2, 1, 1}, { 3, 1, 1 }, { 0, 3, 3 }, { 3, 3, 1 }} };

void input()
{
	int i, j;
	scanf("%d %d", &N, &M);
	for (i = 1; i <= N; i++) {
		for (j = 1; j <= M; j++) {
			scanf("%d", &map[i][j]);
		}
	}
}

int get_max(int sr, int sc)
{
	int max = 0;
	int dir;
	int i, j, k;
	int tmp;
	int nr, nc;

	for (i = 0; i < 6; i++) {
		for (j = 0; j < nd[i]; j++) {
			tmp = map[sr][sc];
			nr = sr; nc = sc;
			for (k = 0; k < 3; k++) {
				dir = lookup[i][j][k];
				nr += dr[dir]; nc += dc[dir];
				if (nr<1 || nc<1 || nr > N || nc > M) break;
				tmp += map[nr][nc];
			}
			if (tmp>max) max = tmp;
		}
	}
	return max;
}

int get_ans()
{
	int i, j;
	int ans = 0;
	int comp;

	for (i = 1; i <= N; i++) {
		for (j = 1; j <= M; j++) {
			comp = get_max(i, j);
			if (comp > ans) ans = comp;
		}
	}
	return ans;
}
int main()
{
	int sol;
	input();
	sol = get_ans();
	printf("%d", sol);
}
